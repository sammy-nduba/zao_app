



export class NewsArticle {
    constructor(id, title, summary, author, readTime, likes, imageUrl, category) {
      this.id = id;
      this.title = title;
      this.summary = summary;
      this.author = author;
      this.readTime = readTime;
      this.likes = likes;
      this.imageUrl = imageUrl;
      this.category = category;
    }
  }